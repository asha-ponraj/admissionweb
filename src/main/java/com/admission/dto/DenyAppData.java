package com.admission.dto;

public class DenyAppData {
	private int id;
	private String reason;
	
	public DenyAppData() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}
	
	
}
