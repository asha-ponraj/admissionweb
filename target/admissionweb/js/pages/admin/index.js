function exitSystem() {
	$.messager.defaults.ok = '确定';
	$.messager.defaults.cancel = '取消';
	var tip = '确定要退出系统吗?'; 
	$.messager.confirm('温馨提示', tip,
		function(sure) {
			if(sure) {
				location = "logout.html";
			}
		});
}

